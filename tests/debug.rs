use stellar_strkey::*;

extern crate proptest;

#[cfg(feature = "alloc")]
fn make_payload<const N: usize>(arr: [u8; N]) -> Vec<u8> {
    arr.to_vec()
}

#[cfg(not(feature = "alloc"))]
fn make_payload<const N: usize>(arr: [u8; N]) -> heapless::Vec<u8, 64> {
    heapless::Vec::from_slice(&arr).unwrap()
}

